col = "Regent College London"

# a) Print index 0 to 6
print(col[0:7])

# b) Print index 0, 7, and 15 together
print(col[0], col[7], col[15])

# c) Print "College" only
print(col[7:14])

# d) Print "gentle"
print(col[2:6] + col[10:12])

# e) Print last 3 characters
print(col[-3:])

# f) Print only "Regent London"
print(col[0:7] + " " + col[15:])
